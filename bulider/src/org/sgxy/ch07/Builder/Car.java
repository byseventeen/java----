package org.sgxy.ch07.Builder;
/*
 * 产品类 -- 复杂对象
 */
public class Car {
	private String body;
	private String faDongJi;
	private String paiQiGuan;
	private String lunTai;
	public String getBody() {
		return body;
	}
	public void setBody(String body) {
		this.body = body;
	}
	public String getFaDongJi() {
		return faDongJi;
	}
	public void setFaDongJi(String faDongJi) {
		this.faDongJi = faDongJi;
	}
	public String getPaiQiGuan() {
		return paiQiGuan;
	}
	public void setPaiQiGuan(String paiQiGuan) {
		this.paiQiGuan = paiQiGuan;
	}
	public String getLunTai() {
		return lunTai;
	}
	public void setLunTai(String lunTai) {
		this.lunTai = lunTai;
	}
}
